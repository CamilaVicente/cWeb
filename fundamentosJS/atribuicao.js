const a = 7 // o simbolo = representa a atribuição
let b = 3

 // aqui estou fazendo uma atribuiçao adtiva  ou seja junto com a atribuiçao estou fazendo uma adição
b += a //b = b + a = 10
console.log(b)

 //atribuicao subtrativa
b -= a // b = b - a = 4
console.log(b)

//atribuicao multiplicativa
b *= a // b = b * a = 21
console.log(b)

//atribuicao divisiva
b /= a // b = b / a = 3
console.log(b)

//atribuicao Modular
b %= 2 // b = b % 2 = 1
console.log(b)